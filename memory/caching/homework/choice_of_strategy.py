"""
1. Високонавантажений REST API для каталогу товарів

Клієнт має інтернет-магазин з мільйонами товарів. Вимога - 95-й перцентиль відповіді < 80 мс.
База даних витримує лише ~700 RPS, але система отримує піки у 4000 RPS під час розпродажів.
Дані про товар рідко змінюються, але зміни повинні одразу бути видимими.
Інвалідація кешу - відповідальність бекенду.
Потрібно вирішити, як кешувати читання.
"""
"""
    Стратегія: cache-aside read
    Бекенд сам керує кешем і інвалідацією. Дані читаються часто, тому при miss підтягуємо з БД і кладемо в кеш.
"""

"""
2. Пайплайн обробки подій

У системі обробляються події з IoT-девайсів.
Запис у основну БД повинен бути гарантовано збережений, але можливе тимчасове кешування, щоб зменшити навантаження.
Інфраструктура клієнта обмежує кількість транзакцій у БД до 1000 операцій/сек.
Важливо - дані не можна втратити, але можна доставити з невеликою затримкою.
"""
"""
    Стратегія: cache-behind
    Запис йде спочатку в кеш/буфер, а в БД — асинхронно. Це зменшує навантаження на БД і допускає невелику затримку.
"""

"""
3. Платформа для рекомендацій на основі LLM

Сервіс генерує персоналізовані рекомендації на основі профілю користувача.
Вартість одного запиту до ML/LLM API - висока, тому кількість викликів потрібно мінімізувати.
Профіль змінюється рідко, але запити зростають до 10k RPS.
При цьому клієнт вимагає не повертати старі рекомендації, якщо профіль реально оновлено.
Потрібно вирішити, як читати та оновлювати кеш для рекомендацій.
"""
"""
    Стратегія: cache-aside read + cache-aside write
    Дорогі обчислення кешуються при першому запиті. При зміні профілю кеш явно інвалідиться або оновлюється.
"""

"""
4. Фінансовий сервіс, що виконує транзакції

Сервіс обробляє банківські транзакції та баланси.
Будь-які операції на балансі повинні оновлюватися одночасно в кеші та в основній БД.
Кеш використовується для швидкого читання балансу.
Помилка запису у БД або в кеш не може призвести до розсинхронізації.
Треба визначити стратегію для запису.
"""
"""
    Стратегія: cache-through write
    Запис іде через кеш, який сам синхронно оновлює БД. Це гарантує узгодженість між кешем і сховищем.
"""

"""
5. CDN-like сервіс для контенту

Клієнт планує запуск рекламної кампанії, де більшість користувачів відкриватимуть певні сторінки одночасно.
Контент статичний, але дуже важкий (5–20 МБ).
Вимога - прогріти кеши за кілька годин до старту кампанії.
Навантаження на origin-сервер обмежене (не більше 200 RPS).
Потрібно визначити, коли і як наповнювати кеш.
"""
"""
    Стратегія: cache-ahead
    Кеш прогрівається заздалегідь до навантаження. Це зменшує звернення до origin під час піку.
"""

"""
6. Система обліку замовлень у логістичній компанії

Кожна зміна статусу замовлення (₴ -> picked -> in transit -> delivered) має записуватися в історію.
Записів багато - до 20k подій/сек.
Сервіс повинен швидко приймати оновлення, але читання історії статусів не критичне.
Замовнику важливо, щоб БД не була перевантажена постійними записами.
Дані можуть потрапляти у БД з невеликою затримкою, але не повинні втрачатися.
"""
"""
    Стратегія: cache-behind
    Система швидко приймає записи в кеш і пізніше пише в БД. Це знижує навантаження при великому потоці подій.
"""
